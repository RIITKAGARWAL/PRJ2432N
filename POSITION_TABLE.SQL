SET SERVEROUTPUT ON;

BEGIN
   EXECUTE IMMEDIATE 'CREATE TABLE POSITION (
       POS_ID VARCHAR2(6) PRIMARY KEY,
       POS_NM VARCHAR2(15) NOT NULL,
       BASE_SAL DECIMAL(7,2) NOT NULL,
       POS_LVL CHAR(1) NOT NULL,
		 DEPT_ID VARCHAR2(6)
       REFERENCES DEPARTMENT(DEPT_ID)
   )';

   DBMS_OUTPUT.PUT_LINE('POSITION TABLE HAS BEEN CREATED SUCCESSFULLY');
EXCEPTION
   WHEN OTHERS THEN
      DBMS_OUTPUT.PUT_LINE('ERROR IS: ' || SQLERRM);
END;
/
