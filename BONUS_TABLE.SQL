SET SERVEROUTPUT ON;

BEGIN
   EXECUTE IMMEDIATE 'CREATE TABLE BONUS (
      BNS_ID VARCHAR2(7) PRIMARY KEY,
      EMP_ID VARCHAR2(6) CONSTRAINT FK_EMPY REFERENCES EMPLOYEE(EMP_ID),
      PRL_ID VARCHAR2(6) CONSTRAINT FK_PROL REFERENCES PAYROLL(PRL_ID),
	   BNS_TYPE VARCHAR2(15) NOT NULL,
      BNS_AMT NUMBER(6,2) NOT NULL CHECK (BNS_AMT >= 0),
  	   ISSUE_DT DATE NOT NULL,
	   REMARKS VARCHAR2(255) NULL
)';

   DBMS_OUTPUT.PUT_LINE('BONUS TABLE HAS BEEN CREATED SUCCESSFULLY');
EXCEPTION
   WHEN OTHERS THEN
      DBMS_OUTPUT.PUT_LINE('ERROR: ' || SQLERRM);
END;
/
