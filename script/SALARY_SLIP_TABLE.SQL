SET SERVEROUTPUT ON;

BEGIN
   EXECUTE IMMEDIATE 'CREATE TABLE SALARY_SLIP (
      SAL_SLIP VARCHAR2(7) PRIMARY KEY,
      EMP_ID VARCHAR2(6) CONSTRAINT FK_EMPLOYE REFERENCES EMPLOYEE(EMP_ID),
      PRL_ID VARCHAR2(6) CONSTRAINT FK_PROLL REFERENCES PAYROLL(PRL_ID),
      BNS_ID VARCHAR2(6) CONSTRAINT FK_BNS REFERENCES BONUS(BNS_ID),
      ADV_ID VARCHAR2(6) CONSTRAINT FK_ADV REFERENCES ADVANCE(ADV_ID),
	   COMP_ID VARCHAR2(6) CONSTRAINT FK_COMP REFERENCES COMPLIANCE(COMP_ID),
	   PAYM_MT_YR VARCHAR(7) NOT NULL,
	   SAL_BRKDWN VARCHAR2(100) NOT NULL,
	   TAX_DTLS VARCHAR2(100) NULL,
	   NET_SAL NUMBER(10,2) NOT NULL CHECK(NET_SAL>=0),
	   PAY_MODE CHAR(20) NOT NULL CHECK(PAY_MODE IN (''C'',''B'',''O'')),
	   TRANS_ID VARCHAR2 (30) NULL
)';

   DBMS_OUTPUT.PUT_LINE('SALARY_SLIP TABLE HAS BEEN CREATED SUCCESSFULLY');
EXCEPTION
   WHEN OTHERS THEN
      DBMS_OUTPUT.PUT_LINE('ERROR: ' || SQLERRM);
END;
