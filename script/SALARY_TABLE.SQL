SET SERVEROUTPUT ON;

BEGIN
   EXECUTE IMMEDIATE 'CREATE TABLE SALARY (
      SAL_ID VARCHAR2(7) PRIMARY KEY,
      EMP_ID VARCHAR2(6) CONSTRAINT FK_EMPYE REFERENCES EMPLOYEE(EMP_ID),
      SAL_SLIP VARCHAR2(7) CONSTRAINT FK_SLP REFERENCES SALARY_SLIP(SAL_SLIP),
      BASIC_PAY NUMBER(7,2) NOT NULL CHECK(BASIC_PAY >=0),
      ALLOWANCES NUMBER(7,2)  NULL CHECK (ALLOWANCES >= 0),
      DEDUCTIONS NUMBER(7,2)  NULL CHECK (DEDUCTIONS >= 0),
      GROSS_SAL NUMBER(7,2) NOT NULL ,
      NET_SAL NUMBER(7,2) NOT NULL,
      PAY_PERIOD_SDT DATE NOT NULL,
      PayPeriodEndDate DATE NOT NULL
 )';

   DBMS_OUTPUT.PUT_LINE('SALARY TABLE HAS BEEN CREATED SUCCESSFULLY');
EXCEPTION
   WHEN OTHERS THEN
      DBMS_OUTPUT.PUT_LINE('ERROR: ' || SQLERRM);
END;
/
