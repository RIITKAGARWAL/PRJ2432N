SET SERVEROUTPUT ON

BEGIN
EXECUTE IMMEDIATE '
CREATE TABLE BANK(
BANK_ID VARCHAR2(6) PRIMARY KEY,
EMP_ID VARCHAR2(6) REFERENCES EMPLOYEE(EMP_ID),
ACC_NM VARCHAR2(40) NOT NULL,
BK_NM VARCHAR2(40) NOT NULL,
ACC_NO VARCHAR(17) UNIQUE NOT NULL,
IFSC VARCHAR2(11) NOT NULL,
BK_BRANCH VARCHAR2(25) NOT NULL
)';
