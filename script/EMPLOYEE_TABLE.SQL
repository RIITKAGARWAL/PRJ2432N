PINCODE CHAR(6) NOT NULL,
PHNO CHAR(10) UNIQUE NOT NULL,
WPHNO CHAR(10) UNIQUE NOT NULL,
EPHNO CHAR(10) UNIQUE NOT NULL,
EMAIL VARCHAR2(100) UNIQUE NOT NULL,
DOJ DATE	NOT NULL,
TYPE CHAR(1) CHECK(TYPE IN(''F'',''C'',''P'')),
