SET SERVEROUTPUT ON;

BEGIN
   EXECUTE IMMEDIATE 'CREATE TABLE ATTENDANCE (
   ATTD_ID VARCHAR2(10) PRIMARY KEY,
   EMP_ID VARCHAR2(6) CONSTRAINT FK_EMP REFERENCES EMPLOYEE(EMP_ID),
   LV_ID VARCHAR2(10) CONSTRAINT FK_LV REFERENCES LEAVE(LV_ID),
   LV_DATE DATE NOT NULL,
   LV_EDT DATE NOT NULL,
   STATUS CHAR(1) NOT NULL CHECK (STATUS IN (''P'', ''A'', ''L'')),
   SHIFT_IN DATE NULL,
   SHIFT_OUT DATE NULL,
   OVERTIME NUMBER(5,2) DEFAULT 0 CHECK (OVERTIME >= 0),
   TOT_WRKHRS NUMBER(5,2) DEFAULT 0 CHECK (TOT_WRKHRS >= 0)
)';

   DBMS_OUTPUT.PUT_LINE('ATTENDANCE TABLE HAS BEEN CREATED SUCCESSFULLY');
EXCEPTION
   WHEN OTHERS THEN
      DBMS_OUTPUT.PUT_LINE('ERROR: ' || SQLERRM);
END;
/